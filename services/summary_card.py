def build_summary_card(
    element_analysis: dict,
    year_fortune: dict,
    career_fortune: dict,
    relationship_fortune: dict,
    ten_gods: dict,
    saju_result: dict,
) -> dict:
    """Build an at-a-glance summary card that changes with the chart."""
    seed = _build_seed(saju_result)
    day_stem = saju_result["saju"]["day"]["stem"]
    day_element = STEMS_BY_KOR[day_stem]["element"]
    month_branch = saju_result["saju"]["month"]["branch"]
    time_branch = (saju_result["saju"]["time"] or saju_result["saju"]["month"])["branch"]
    dominant = _pick_value(element_analysis["dominant"], seed + 3)
    weak = _pick_value(element_analysis["weak"], seed + 7)
    month_star = ten_gods["ten_gods"]["month"] or year_fortune["ten_god"]

    year_trend = _build_year_trend(
        year_ten_god=year_fortune["ten_god"],
        daewoon_ten_god=year_fortune["daewoon_ten_god"],
        month_star=month_star,
        seed=seed,
    )
    wealth = _build_wealth_card(
        dominant=dominant,
        weak=weak,
        day_element=day_element,
        day_stem=day_stem,
        year_ten_god=year_fortune["ten_god"],
        month_star=month_star,
        seed=seed,
    )
    career = _build_career_card(
        dominant=dominant,
        day_stem=day_stem,
        month_branch=month_branch,
        month_star=month_star,
        career_fortune=career_fortune,
        seed=seed,
    )
    relationship = _build_relationship_card(
        dominant=dominant,
        day_stem=day_stem,
        time_branch=time_branch,
        month_star=month_star,
        relationship_fortune=relationship_fortune,
        seed=seed,
    )

    return {
        "year_trend": year_trend,
        "wealth": wealth,
        "career": career,
        "relationship": relationship,
    }


def _build_seed(saju_result: dict) -> int:
    pillars = "".join(
        pillar["kor"]
        for pillar in saju_result["saju"].values()
        if pillar is not None
    )
    return sum(ord(char) for char in pillars)


def _pick(options: list[str], seed: int) -> str:
    return options[seed % len(options)]


def _pick_value(options: list[str], seed: int) -> str:
    return options[seed % len(options)]


def _join_unique(parts: list[str]) -> str:
    result: list[str] = []
    for part in parts:
        if part and part not in result:
            result.append(part)
    return " + ".join(result[:2])


def _build_year_trend(*, year_ten_god: str, daewoon_ten_god: str, month_star: str, seed: int) -> str:
    leading = _pick(YEAR_PRIMARY_LINES[year_ten_god], seed + 11)
    branch = [
        _pick(YEAR_SECONDARY_LINES[daewoon_ten_god], seed + 17),
        _pick(YEAR_MONTH_TEN_GOD_LINES[month_star], seed + 19),
    ]
    return _join_unique([leading, branch[seed % 2]])


def _build_wealth_card(
    *,
    dominant: str,
    weak: str,
    day_element: str,
    day_stem: str,
    year_ten_god: str,
    month_star: str,
    seed: int,
) -> str:
    base_element = dominant if dominant != weak else day_element
    base = _pick(WEALTH_BASE_LINES[base_element], seed + 23)
    variants = [
        _pick(WEALTH_STAR_LINES[year_ten_god], seed + 29),
        _pick(WEALTH_WEAK_LINES[weak], seed + 31),
        _pick(WEALTH_DAY_STEM_LINES[day_stem], seed + 37),
        _pick(WEALTH_STAR_LINES[month_star], seed + 41),
    ]
    return _join_unique([base, variants[seed % len(variants)]])


def _build_career_card(
    *,
    dominant: str,
    day_stem: str,
    month_branch: str,
    month_star: str,
    career_fortune: dict,
    seed: int,
) -> str:
    leading_options = [
        _pick(CAREER_BASE_BY_ELEMENT[dominant], seed + 43),
        _pick(CAREER_MONTH_BRANCH_LINES[month_branch], seed + 47),
        _pick(CAREER_DAY_STEM_LINES[day_stem], seed + 53),
    ]
    trailing_options = [
        _pick(CAREER_TREND_LINES[career_fortune["trend"]], seed + 59),
        _pick(CAREER_TONE_LINES[(career_fortune["tone"], career_fortune["intensity"])], seed + 61),
        _pick(CAREER_MONTH_STAR_LINES[month_star], seed + 67),
    ]
    return _join_unique([
        leading_options[seed % len(leading_options)],
        trailing_options[(seed // 2) % len(trailing_options)],
    ])


def _build_relationship_card(
    *,
    dominant: str,
    day_stem: str,
    time_branch: str,
    month_star: str,
    relationship_fortune: dict,
    seed: int,
) -> str:
    leading_options = [
        _pick(RELATION_BASE_BY_ELEMENT[dominant], seed + 71),
        _pick(RELATION_TIME_BRANCH_LINES[time_branch], seed + 73),
        _pick(RELATION_DAY_STEM_LINES[day_stem], seed + 79),
    ]
    trailing_options = [
        _pick(RELATION_TREND_LINES[relationship_fortune["trend"]], seed + 83),
        _pick(RELATION_INTENSITY_LINES[relationship_fortune["intensity"]], seed + 89),
        _pick(RELATION_MONTH_STAR_LINES[month_star], seed + 97),
    ]
    return _join_unique([
        leading_options[seed % len(leading_options)],
        trailing_options[(seed // 3) % len(trailing_options)],
    ])
