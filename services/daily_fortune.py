def calculate_daily_fortune(saju_result: dict, target_date: date) -> dict:
    """Return the fortune for a specific solar date."""
    day_pillar = get_day_pillar_for_solar_date(target_date)
    saju = saju_result["saju"]
    day_stem = saju["day"]["stem"]
    month_branch = saju["month"]["branch"]
    year_stem = saju["year"]["stem"]
    time_branch = saju["time"]["branch"] if saju.get("time") else None
    ten_god = calculate_ten_god_for_stem(day_stem, day_pillar["stem"])
    rule = DAILY_RULES[ten_god]
    natal_day_pillar_kor = saju["day"]["kor"]
    natal_day_pillar_line = DAY_PILLAR_SENTENCES[natal_day_pillar_kor]["daily"]

    sentence_data = load_analysis_sentences()
    ten_gods = calculate_ten_gods(saju)
    month_ten_god = ten_gods["ten_gods"]["month"]
    day_data = sentence_data["day_stem"][day_stem]
    month_data = sentence_data["month_branch"][month_branch]
    year_data = sentence_data["year_stem"][year_stem]
    time_data = sentence_data["time_branch"].get(time_branch) if time_branch else None
    month_ten_god_data = sentence_data["month_ten_god"].get(month_ten_god) if month_ten_god else None
    seed = target_date.toordinal()
    daily_profile = _resolve_daily_profile(day_stem, month_branch)
    headline = _build_daily_headline(
        profile=daily_profile,
        ten_god=ten_god,
        day_pillar_kor=day_pillar["kor"],
        natal_day_stem=day_stem,
        natal_month_branch=month_branch,
        seed=seed,
    )
    rule_explanation = _pick(_rule_explanation_options(ten_god), seed + 1)
    profile_explanation = _pick(_profile_explanation_options(daily_profile), seed + 3)
    explanation = (
        f"{_strip_day_pillar_prefix(natal_day_pillar_line, natal_day_pillar_kor)} "
        f"{_pick(_day_stem_daily_options(day_stem), seed + 9)} "
        f"{rule_explanation} "
        f"{profile_explanation}"
    )
    score = _build_daily_score(
        ten_god=ten_god,
        daily_profile=daily_profile,
        month_branch=month_branch,
        time_branch=time_branch,
        keywords=rule["keywords"],
    )

    context_easy_lines = [
        natal_day_pillar_line,
        _pick(_day_stem_daily_options(day_stem), seed + 11),
        _pick(_month_branch_daily_options(month_branch), seed + 13),
        _pick(day_data["social_reaction"], seed + 3),
        _pick(_day_branch_context_options(day_pillar["branch"]), seed + 15),
    ]
    if month_ten_god_data:
        context_easy_lines.append(
            f"월간 십성 기준으로 보면 {_pick(month_ten_god_data['personality_modifier'], seed + 17).lower()}"
        )

    context_real_lines = [
        f"원국 일주 {natal_day_pillar_kor}{_object_particle(natal_day_pillar_kor)} 기준으로 보면 {_strip_day_pillar_prefix(natal_day_pillar_line, natal_day_pillar_kor)}",
        _pick(day_data["speech_style"], seed + 7),
        _pick(month_data["work_adaptation"], seed + 11),
        _pick(month_data["money_habit"], seed + 13),
        _pick(year_data["first_impression"], seed + 17),
    ]
    if time_data:
        context_real_lines.append(_pick(time_data["intimate_reaction"], seed + 19))

    context_action_lines = _daily_execution_action_lines(
        score=score["value"],
        ten_god=ten_god,
        keywords=rule["keywords"],
        seed=seed + 21,
    )
    advice = context_action_lines[0]

    strength_lines = [
        _pick(_daily_strength_options(ten_god), seed + 27),
        _pick(_day_stem_strength_options(day_stem), seed + 29),
        _pick(_month_branch_strength_options(month_branch), seed + 31),
    ]
    risk_lines = [
        _pick(_daily_risk_options(ten_god), seed + 33),
        _pick(_day_stem_risk_options(day_stem), seed + 35),
        _pick(_month_branch_risk_options(month_branch), seed + 37),
    ]
    if time_branch and time_data:
        risk_lines.append(_pick(time_data["intimate_reaction"], seed + 31))

    section = build_daily_section(
        headline=headline,
        explanation=explanation,
        advice=advice,
        keywords=rule["keywords"],
        seed=seed,
        context_easy_lines=context_easy_lines,
        context_real_lines=context_real_lines,
        context_action_lines=context_action_lines,
        strength_lines=strength_lines,
        risk_lines=risk_lines,
    )
    return {
        "date": target_date.isoformat(),
        "pillar": day_pillar["kor"],
        "hanja": day_pillar["hanja"],
        "stem": day_pillar["stem"],
        "branch": day_pillar["branch"],
        "ten_god": ten_god,
        "headline": section["one_line"],
        "summary": (
            f"{natal_day_pillar_kor} 일주를 기준으로 보면 "
            f"{_pick(_day_stem_headline_options(day_stem), seed + 39)} "
            f"오늘의 핵심은 {rule['keywords'][0]}과 {rule['keywords'][1]}이고, "
            f"{_pick(_month_branch_headline_options(month_branch), seed + 41)}"
        ),
        "explanation": " ".join(section["easy_explanation"]),
        "advice": " ".join(section["action_advice"]),
        "section": section,
        "keywords": rule["keywords"],
        "score": score,
    }


def _pick(options: list[str], seed: int) -> str:
    return options[seed % len(options)]


def _daily_execution_action_lines(*, score: int, ten_god: str, keywords: list[str], seed: int) -> list[str]:
    """Return concrete action advice without repeating natal-pillar explanations."""
    bucket = _score_action_bucket(score)
    lines = [
        _pick(ACTION_BY_SCORE_BUCKET[bucket], seed),
        _pick(ACTION_BY_TEN_GOD[ten_god], seed + 3),
    ]
    keyword_options = _keyword_action_options(keywords)
    lines.append(_pick(keyword_options, seed + 7))

    unique_lines: list[str] = []
    seen: set[str] = set()
    for line in lines:
        if line in seen:
            continue
        unique_lines.append(line)
        seen.add(line)
    fallback_options = [*ACTION_BY_SCORE_BUCKET[bucket], *ACTION_BY_TEN_GOD[ten_god], *keyword_options]
    offset = 11
    while len(unique_lines) < 3:
        fallback = _pick(fallback_options, seed + offset)
        offset += 1
        if fallback not in seen:
            unique_lines.append(fallback)
            seen.add(fallback)
    return unique_lines[:3]


def _score_action_bucket(score: int) -> str:
    if score >= 90:
        return "very_high"
    if score >= 80:
        return "high"
    if score >= 65:
        return "good"
    if score >= 50:
        return "normal"
    if score >= 35:
        return "caution"
    return "defense"


def _keyword_action_options(keywords: list[str]) -> list[str]:
    options: list[str] = []
    for keyword in keywords:
        options.extend(ACTION_BY_KEYWORD.get(keyword, []))
    return options or ACTION_BY_SCORE_BUCKET["normal"]


def _resolve_daily_profile(day_stem: str, month_branch: str) -> str:
    if day_stem in {"갑", "을"} or month_branch in {"인", "묘"}:
        return "planner"
    if day_stem == "기" or month_branch in {"묘", "미"}:
        return "connector"
    if day_stem in {"병", "정"} or month_branch in {"사", "오"}:
        return "driver"
    if day_stem in {"무", "기"} or month_branch in {"축", "진", "미", "술"}:
        return "stabilizer"
    if day_stem in {"경", "신"} or month_branch in {"신", "유"}:
        return "precision"
    return "observer"


def _build_daily_score(
    *,
    ten_god: str,
    daily_profile: str,
    month_branch: str,
    time_branch: str | None,
    keywords: list[str],
) -> dict:
    factors = [
        {
            "name": "오늘 십성",
            "value": ten_god,
            "delta": TEN_GOD_SCORE[ten_god],
        },
        {
            "name": "오늘 반응형",
            "value": PROFILE_LABELS[daily_profile],
            "delta": PROFILE_SCORE[daily_profile],
        },
        {
            "name": "월지 보정",
            "value": format_branch_label(month_branch),
            "delta": MONTH_BRANCH_SCORE[month_branch],
        },
    ]
    score = DAILY_SCORE_BASE + sum(item["delta"] for item in factors)

    if time_branch:
        time_delta = TIME_BRANCH_SCORE[time_branch]
        factors.append(
            {
                "name": "시지 보정",
                "value": format_branch_label(time_branch),
                "delta": time_delta,
            }
        )
        score += time_delta

    keyword_delta = _keyword_adjustment(keywords)
    factors.append(
        {
            "name": "키워드 보정",
            "value": ", ".join(keywords[:3]),
            "delta": keyword_delta,
        }
    )
    score += keyword_delta

    combo_delta, combo_label = _combo_adjustment(
        ten_god=ten_god,
        daily_profile=daily_profile,
        month_branch=month_branch,
        time_branch=time_branch,
    )
    if combo_delta or combo_label:
        factors.append(
            {
                "name": "조합 보정",
                "value": combo_label,
                "delta": combo_delta,
            }
        )
        score += combo_delta

    bounded_score = _normalize_daily_score(score)
    grade, label = _score_grade_and_label(bounded_score)
    summary = _build_score_summary(
        score=bounded_score,
        label=label,
        ten_god=ten_god,
        daily_profile=daily_profile,
        month_branch=month_branch,
    )
    return {
        "title": "오늘의 운세 활용도",
        "value": bounded_score,
        "grade": grade,
        "label": label,
        "summary": summary,
        "factors": factors,
    }


def _keyword_adjustment(keywords: list[str]) -> int:
    score = 0
    for keyword in keywords[:3]:
        if keyword in POSITIVE_KEYWORDS:
            score += 3
        elif keyword in NEGATIVE_KEYWORDS:
            score -= 3
    return max(-6, min(6, score))


def _normalize_daily_score(raw_score: int) -> int:
    raw_range = DAILY_SCORE_RAW_MAX - DAILY_SCORE_RAW_MIN
    if raw_range <= 0:
        return DAILY_SCORE_MIN
    ratio = (raw_score - DAILY_SCORE_RAW_MIN) / raw_range
    normalized = round(DAILY_SCORE_MIN + ratio * (DAILY_SCORE_MAX - DAILY_SCORE_MIN))
    return max(DAILY_SCORE_MIN, min(DAILY_SCORE_MAX, normalized))


def _combo_adjustment(
    *,
    ten_god: str,
    daily_profile: str,
    month_branch: str,
    time_branch: str | None,
) -> tuple[int, str]:
    delta = 0
    reasons: list[str] = []

    if ten_god in {"식신", "정재", "정인"} and daily_profile in {"stabilizer", "precision", "planner"}:
        delta += 6
        reasons.append(f"{ten_god} + {PROFILE_LABELS[daily_profile]}")

    if ten_god in {"겁재", "편관", "상관"} and daily_profile in {"driver", "connector"}:
        delta -= 6
        reasons.append(f"{ten_god} + {PROFILE_LABELS[daily_profile]}")

    if month_branch in {"축", "진", "미", "술"} and ten_god in {"정재", "정관", "정인"}:
        delta += 3
        reasons.append(f"{format_branch_label(month_branch)}의 유지력")

    if month_branch in {"사", "오"} and ten_god in {"상관", "겁재", "편관"}:
        delta -= 3
        reasons.append(f"{format_branch_label(month_branch)}의 과속성")

    if time_branch in {"축", "진", "미", "술"} and ten_god in {"정재", "정인"}:
        delta += 2
        reasons.append(f"{format_branch_label(time_branch)}의 안정 보정")

    if time_branch in {"사", "오"} and ten_god in {"상관", "편관"}:
        delta -= 2
        reasons.append(f"{format_branch_label(time_branch)}의 긴장 보정")

    return max(-10, min(10, delta)), ", ".join(reasons)


def _score_grade_and_label(score: int) -> tuple[str, str]:
    for threshold, grade, label in SCORE_GRADE_RULES:
        if score >= threshold:
            return grade, label
    return "D", "방어가 우선인 날"


