def map_day_stem_to_user_type(day_stem: str) -> str:
    if day_stem in {"갑", "을"}:
        return "stable"
    if day_stem in {"병", "정"}:
        return "drive"
    if day_stem in {"무", "기"}:
        return "change"
    if day_stem in {"경", "신"}:
        return "relation"
    return "chance"


